package aula07;

public class For {

    public static void main(String[] args) {
        // Exemplo de uso do loop for em Java

        // Loop for para imprimir os números de 1 a 5
        System.out.println("Loop for:");
        for (int i = 1; i <= 5; i++) { // Inicialização; Condição de continuação; Atualização do contador
            System.out.println("Contador: " + i);
        }
    }

}
